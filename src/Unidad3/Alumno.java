package Unidad3;

public class Alumno
{
	private String nc, nom, car;
	private int pro, sem;
	
	public String getNc()
	{
		return nc;
	}
	public void setNc(String nc)
	{
		this.nc = nc;
	}
	public String getNom()
	{
		return nom;
	}
	public void setNom(String nom)
	{
		this.nom = nom;
	}
	public String getCar()
	{
		return car;
	}
	public void setCar(String car)
	{
		this.car = car;
	}
	public int getPro()
	{
		return pro;
	}
	public void setPro(int pro)
	{
		this.pro = pro;
	}
	public int getSem()
	{
		return sem;
	}
	public void setSem(int sem)
	{
		this.sem = sem;
	}
	
	
}
