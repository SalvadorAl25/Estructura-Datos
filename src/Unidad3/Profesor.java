package Unidad3;

public class Profesor
{
	private int nt;
	private String nom;
	private double sue;
	
	public int getNt()
	{
		return nt;
	}
	public void setNt(int nt)
	{
		this.nt = nt;
	}
	public String getNom()
	{
		return nom;
	}
	public void setNom(String nom)
	{
		this.nom = nom;
	}
	public double getSue()
	{
		return sue;
	}
	public void setSue(double sue)
	{
		this.sue = sue;
	}
}
