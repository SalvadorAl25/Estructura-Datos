package UNIDAD1;

public class Libros
{
	private String tit,autor,edi;
	private int isbn;
	private double pre;
	
	public String getTit()
	{
		return tit;
	}
	public void setTit(String tit)
	{
		this.tit = tit;
	}
	public String getAutor()
	{
		return autor;
	}
	public void setAutor(String autor)
	{
		this.autor = autor;
	}
	public String getEdi()
	{
		return edi;
	}
	public void setEdi(String edi)
	{
		this.edi = edi;
	}
	public int getIsbn()
	{
		return isbn;
	}
	public void setIsbn(int isbn)
	{
		this.isbn = isbn;
	}
	public double getPre()
	{
		return pre;
	}
	public void setPre(double pre)
	{
		this.pre = pre;
	}
}
